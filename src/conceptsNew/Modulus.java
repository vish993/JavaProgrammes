package conceptsNew;
public class Modulus {
	public static void main(String[] args) {
		int x = 5;
	    int y = 2;
	    System.out.println(x % y);
	    
	    int q = 5; // always show remainder
	    q %= 2; // q=q%3
	    System.out.println(q);
	    
	    float p = 5; // always show ans in quotient either we use float or int
	    p /= 2; // p=p/3
	    System.out.println(p);
	    
	    int s = 5;// Increases the value of a variable by 1
	    ++s;
	    System.out.println(s);
		
	    int t = 5;// Decreases the value of a variable by 1
	    --t;
	    System.out.println(t);
	    
	    int l = 5;
	   l += 3;
	    System.out.println(l);
	    
	    int j = 5; //
		   j -= 3;
		    System.out.println(j);
		    
		    int k = 5;
		    k *= 3;
		    System.out.println(k);
		    
		    int n = 5;
		    n &= 3;
		    System.out.println(n);
	    
	    
	}

}
