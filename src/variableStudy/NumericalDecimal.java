package variableStudy;

public class NumericalDecimal {

	public static void main(String[] args) {
		// Primitive Data type float, double
				System.out.println("Primitive Data Type Study :-"+"\n");
				System.out.println("Numerical & Decimal:-");
				
				// float
				System.out.println("1. float:-");
				// 1. variable declaration
				float x;
				// 2. variable initialization
				x=123456789.12f;
				// 3. usage
				System.out.println("The value of a is "+x+"."+"\n");
				
				//double
				double b;
				b=5.1234567890123457;
				System.out.println("2. double:-");
				System.out.println("The value of b is "+b+"."+"\n");
				
				double divide;
				divide=b/x;
				System.out.println("divisin of float/double is "+divide);
	}

}
