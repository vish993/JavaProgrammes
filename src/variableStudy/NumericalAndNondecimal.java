package variableStudy;

public class NumericalAndNondecimal {

	public static void main(String[] args) {
		// Primitive Data type byte,short,int,long
		System.out.println("Primitive Data Type Study :-"+"\n");
		System.out.println("Numerical & Non-Decimal:-");
		
		// byte   
		System.out.println("1. byte:-");
		// 1. variable declaration
		byte a;
		// 2. variable initialization
		a=-125;
		// 3. usage
		System.out.println("The value of a is "+a+"."+"\n");
		
		//short
		short b;
		b=-32548;
		System.out.println("2. short:-");
		System.out.println("The value of b is "+b+"."+"\n");
		
		//int
		int c;
		c=325846871;
		System.out.println("3. int:-");
		System.out.println("The value of c is "+c+"."+"\n");
		
		// long
		long d;
		d=1234567891234567891l;
		System.out.println("4. long:-");
		System.out.println("The value of d is "+d+"."+"\n");
			
	}

}
