package variableStudy;

public class PrimitiveDataTypeStudy {

	public static void main(String[] args) {
		String name;
		name="Vishal Namdeo Date.";
		char grade;
		grade='O';
		short rollNo;
		rollNo=141;
		byte age;
		age=29;
		float height;
		height=5.8f;
		float weight;
		weight=78.9f;
		
		System.out.println("My name is "+name);
		System.out.println("My Roll No. is "+rollNo+".");
		System.out.println("I am "+age+" years old.");
		System.out.println("I am "+height+" ft tall.");
		System.out.println("My weight is "+weight+"kgs.");
		System.out.println("I secured grade "+"'"+grade+"'" +" in Velocity.");
		

	}

}
